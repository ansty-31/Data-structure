typedef int KeyType;
typedef char InfoType;

typedef struct
{
    KeyType key;     //关键字
    InfoType data;   //数据项
}RecType;   //每个数据元素的定义

//直接插入排序，局部有序，n、n^2、n^2，1（最好的、平均的、最坏的，空间）
void InsertSort(RecType R[], int n)
{
    int i,j;
    RecType temp;
    for(i=1;i<n;i++)
    {
        if(R[i].key<R[i-1].key)
        {
            temp=R[i];
            j=i-1;
            do
            {
                R[j+1]=R[j];
                j--;
            } while (j>=0&&R[j].key>temp.key);
            R[j+1]=temp;
        }
    }
}

//折半插入排序,由于局部是有序的，所以可以在局部内折半，相比直接插入，性能并没有明显提示，只是减少了比较次数而已
void BinInsertSort(RecType R[],int n)
{
    int i,j,low,high,mid;
    RecType tmp;
    for(i=1;i<n;i++)
    {
        if(R[i].key<R[i-1].key)
        {
            tmp=R[i];
            low = 0;high = i-1;
            while (low<=high)
            {
                mid=(low+high)/2;
                if(tmp.key<R[mid].key) high=mid-1;
                else low=mid+1;
            }
            for(j=i-1;j>=high+1;j--)
            {
                R[j+1]=R[j];
            }
            R[high+1]=tmp;
        }
    }
}

//希尔排序（分组插入排序），一般为n^1.3，增量d每次对半分，不稳定
void ShellSort(RecType R[],int n)
{
    int i,j,d;
    RecType tmp;
    d=n/2;
    while (d>0)
    {
        for(i=d;i<n;i++)
        {
            tmp=R[i];
            j=i-d;
            while (j>=0&&tmp.key<R[j].key)
            {
                R[j+d]=R[j];
                j=j-d;
            }
            R[j+d]=tmp;
        }
        d=d/2;
    }
} //每次插入和比较都在同组内，增量1变成了增量d



